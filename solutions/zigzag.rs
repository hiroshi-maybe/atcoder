fn main() {
    setup_out!(put, puts);

    let n = readln!(usize);
    let a = readln!([i64]);
    let mut cnt = vec![vec![0; 100_001]; 2];
    for i in 0..n {
        cnt[i % 2][a[i] as usize] += 1;
    }
    let mut cnts = vvec![(0, 0); 2; 2];
    for i in 0..2 {
        for j in 0..cnt[i].len() {
            let x = (cnt[i][j], j);
            cnts[i][1].setmax(x);
            let x = cnts[i][1];
            let y = cnts[i][0];
            if x > y {
                cnts[i][0] = x;
                cnts[i][1] = y;
            }
        }
    }

    let mut res = n;
    for i in 0..2 {
        for j in 0..2 {
            let a = cnts[0][i];
            let b = cnts[1][j];
            let x = n / 2 - a.0;
            let y = n / 2 - b.0;
            let z = if a.1 == b.1 { n } else { x + y };
            //dbgln!(a, b, x, y, z);
            res.setmin(z);
        }
    }

    puts!("{}", res);
}


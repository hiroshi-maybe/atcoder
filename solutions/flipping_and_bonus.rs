fn solve() -> i64 {
    let (n, m) = readln!(usize, usize);
    let x = readln!([i64]);
    let c = readlns!(usize, i64; m);
    let mut b = vec![0; n + 1];
    for (c, y) in c {
        b[c] = y;
    }

    let mut dp = vvec!(10i64.pow(15) * -1; n + 1; n + 1);
    dp[0][0] = 0;

    for i in 0..n {
        for c in (0..n).rev() {
            let cur = dp[i][c];
            dp[i + 1][c + 1].setmax(cur + x[i] + b[c + 1]);
            dp[i + 1][0].setmax(cur);
        }
        // dbgln!(i, dp[i + 1]);
    }

    dp[n].iter().copied().max().unwrap()
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", res);
}


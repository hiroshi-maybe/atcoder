fn solve() -> i32 {
    let n = readln!(usize);
    let r = readln!([i32]);

    let mut dp = vvec!(-(n as i32) -1; n+1; 3);
    dp[n][2] = 0;
    for i in 0..n {
        let mut dp2 = vvec!(-(n as i32) -1; n+1; 3);
        for last in 0..=n {
            for ud in 0..3 {
                let cur = dp[last][ud];
                if last == n {
                    if ud == 2 {
                        dp2[i][2].setmax(cur + 1);
                        dp2[last][ud].setmax(cur);
                    }
                } else {
                    dp2[last][ud].setmax(cur);
                    match ud {
                        2 | 1 if r[last] > r[i] => {
                            dp2[i][0].setmax(cur + 1);
                        }
                        2 | 0 if r[last] < r[i] => {
                            dp2[i][1].setmax(cur + 1);
                        }
                        _ => {}
                    };
                }
            }
        }
        std::mem::swap(&mut dp, &mut dp2);
    }

    let mut res = 0;
    for last in 0..n {
        for ud in 0..2 {
            res.setmax(dp[last][ud]);
        }
    }
    if res > 2 {
        res
    } else {
        0
    }
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", res);
}


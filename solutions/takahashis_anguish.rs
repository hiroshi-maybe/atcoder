fn dfs(u: usize, viz: &mut Vec<bool>, g: &Vec<Vec<(usize, i64)>>, res: &mut i64) {
    if viz[u] {
        return;
    }
    viz[u] = true;

    for &(v, cost) in &g[u] {
        res.setmin(cost);
        dfs(v, viz, g, res);
    }
}

fn solve() -> i64 {
    let n = readln!(usize);
    let xs = readln!([usize1]);
    let cs = readln!([i64]);

    let mut g = vec![vec![]; n];
    let mut in_deg = vec![0; n];
    for u in 0..n {
        g[u].push((xs[u], cs[u]));
        in_deg[xs[u]] += 1;
    }

    let mut q = VecDeque::new();
    for u in 0..n {
        if in_deg[u] == 0 {
            q.push_back(u);
        }
    }
    // dbgln!(in_deg);
    while let Some(u) = q.pop_front() {
        for &(v, _) in &g[u] {
            in_deg[v] -= 1;
            if in_deg[v] == 0 {
                q.push_back(v);
            }
        }
    }

    // dbgln!(in_deg);
    let mut viz = vec![false; n];
    let mut ans = 0;
    for u in (0..n).filter(|&u| in_deg[u] > 0) {
        if !viz[u] {
            let mut res = i64::max_value();
            dfs(u, &mut viz, &g, &mut res);
            ans += res;
        }
    }

    ans
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", res);
}


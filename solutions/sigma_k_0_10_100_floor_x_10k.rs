fn solve() -> Vec<i64> {
    let x = readln!([u8])
        .iter()
        .map(|&x| (x - b'0') as i64)
        .collect_vec();
    //dbgln!(x);
    let n = x.len();
    let mut sum = x.iter().sum::<i64>();
    let mut res = vec![];
    let mut carry = 0;

    for (i, dig) in x.iter().rev().copied().chain(0..).enumerate() {
        if i >= n && carry == 0 {
            break;
        }
        let y = sum + carry;
        //dbgln!(i, dig, sum, carry, y);
        res.push(y % 10);
        carry = y / 10;
        sum -= dig;
    }
    res.reverse();
    res
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    for r in res {
        put!("{}", r);
    }
    puts!();
}


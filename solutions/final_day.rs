/// 11/20/2021
///
/// 9:15-9:58 AC
///
/// https://atcoder.jp/contests/abc228/editorial/2943
///

fn solve() -> Vec<bool> {
    let (n, k) = readln!(usize, usize);
    let p = readlns!([i32]; n);

    let mut p = p
        .iter()
        .enumerate()
        .map(|(i, vs)| (vs.iter().sum::<i32>(), i))
        .collect_vec();
    p.sort_unstable_by_key(|x| Reverse(*x));

    let st: &mut Vec<(i32, Vec<usize>)> = &mut vec![];
    for &(v, i) in &p {
        if st.is_empty() || st.last().unwrap().0 != v {
            st.push((v, vec![]));
        }

        st.last_mut().unwrap().1.push(i);
    }

    //dbgln!(st);

    let mut threshold = 0;
    let mut sum = 0;
    for (v, xs) in st {
        if sum <= k - 1 {
            threshold = *v;
        }
        sum += xs.len();
    }
    //dbgln!(threshold);

    let mut res = vec![false; n];
    for (v, i) in p {
        res[i] = v + 300 >= threshold;
    }
    res
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    for x in res {
        puts!("{}", if x { "Yes" } else { "No" });
    }
}


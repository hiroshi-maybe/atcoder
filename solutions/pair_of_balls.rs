fn solve() -> bool {
    let (n, m) = readln!(usize, usize);
    let mut g = vvec!(vec![]; n);
    for _ in 0..m {
        let _k = readln!(usize);
        let a = readln!([usize1]);
        for vs in a.windows(2) {
            g[vs[0]].push(vs[1]);
        }
    }

    !has_cyclic(&g)
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", if res { "Yes" } else { "No" });
}


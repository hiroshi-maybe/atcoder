fn f(a: &Vec<i64>, k: usize) -> i64 {
    let n = a.len();
    let first = a.iter().position(|&x| x >= 0).unwrap_or(n);

    let mut res = 10i64.pow(17);

    //dbgln!(a, first);

    let mut l = 0;
    for i in first..=n {
        while i - l > k {
            l += 1;
        }
        //dbgln!(l, i);
        if i - l == k {
            let ldist = a[l].abs();
            let rdist = max(a[i - 1], 0);
            res.setmin(ldist + rdist * 2);
        }
    }
    //dbgln!(a, first, res);

    res
}

#[allow(dead_code)]
fn solve_v1() -> i64 {
    let (_n, k) = readln!(usize, usize);
    let a = readln!([i64]);

    let mut res = 10i64.pow(17);
    res.setmin(f(&a, k));
    let mut a = a.iter().map(|&x| -x).collect_vec();
    a.reverse();
    res.setmin(f(&a, k));

    res
}

fn solve() -> i64 {
    let (n, k) = readln!(usize, usize);
    let a = readln!([i64]);

    let mut res = 10i64.pow(17);
    for i in (0..).take_while(|&i| i + k - 1 < n) {
        let (l, r) = (a[i], a[i + k - 1]);
        res.setmin(min(l.abs(), r.abs()) + r - l);
    }
    res
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", res);
}


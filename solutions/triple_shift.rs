fn inv(a: &[i64]) -> usize {
    (0..a.len())
        .map(|i| a[..i].iter().filter(|&&b| b < a[i]).count())
        .sum::<usize>()
}

fn solve() -> bool {
    let n = readln!(usize);
    let a = readln!([i64]);
    let b = readln!([i64]);

    let (mut sa, mut sb) = (a.clone(), b.clone());
    sa.sort_unstable();
    sb.sort_unstable();

    if sa != sb {
        return false;
    }

    sa.dedup();
    if sa.len() != n {
        return true;
    }

    inv(&a) % 2 == inv(&b) % 2
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", if res { "Yes" } else { "No" });
}


fn solve() -> i64 {
    let (n, m) = readln!(usize, usize);
    let h = readln!([i64]);
    let mut g = vec![vec![]; n];
    for (u, v) in readlns!(usize1, usize1; m) {
        g[u].push(v);
        g[v].push(u);
    }

    let inf = 10i64.pow(17);
    let mut ds = vec![inf; n];
    let mut q = BinaryHeap::new();
    q.push((Reverse(0), 0));
    ds[0] = 0;

    while let Some((Reverse(d), u)) = q.pop() {
        if d != ds[u] {
            continue;
        }
        for &v in &g[u] {
            let dd = d + max(0, h[v] - h[u]);
            if dd < ds[v] {
                ds[v] = dd;
                q.push((Reverse(dd), v));
            }
        }
    }

    ds.iter()
        .enumerate()
        .map(|(v, &d)| h[0] - h[v] - d)
        .max()
        .unwrap()
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", res);
}


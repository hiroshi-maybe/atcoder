fn solve() -> i64 {
    let (n, k) = readln!(usize, i64);
    let a = readln!([i64]);

    let mut steps = vec![(-1, 0); n];
    let mut cur = 0;
    let mut p = 0;
    let mut last_i = n;
    for i in 0..n + 10 {
        cur += a[p];
        p = (cur % n as i64) as usize;
        if steps[p].0 >= 0 {
            last_i = i + 1;
            break;
        } else {
            steps[p] = (cur, i + 1);
        }
    }

    for &(x, i) in &steps {
        if i as i64 == k {
            return x;
        }
    }

    assert!(steps[p].0 >= 0);
    let cycle_step = (last_i - steps[p].1) as i64;
    let cycle_cnt = cur - steps[p].0;

    let mut res = cur;
    let k = k - last_i as i64;
    res += k / cycle_step * cycle_cnt;
    let k = k % cycle_step;
    for _ in 0..k {
        p = (res % n as i64) as usize;
        res += a[p];
    }
    res
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", res);
}


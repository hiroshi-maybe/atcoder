fn solve() -> Option<Vec<i64>> {
    let n = readln!(usize);
    let s = readln!([i64]);

    let mut b = vec![vec![0]; 3];
    for (i, ss) in s.windows(2).enumerate() {
        let last = *b[i % 3].last().unwrap();
        b[i % 3].push(last + ss[1] - ss[0]);
    }

    let mut a = vec![0; n + 2];
    for i in 0..3 {
        let min = *b[i].iter().min().unwrap();
        for (j, &r) in b[i].iter().enumerate() {
            a[3 * j + i] = r + max(-min, 0);
        }
    }

    let dif = s[0] - (a[0] + a[1] + a[2]);
    if dif < 0 {
        None
    } else {
        for i in (0..).step_by(3).take_while(|&i| i < n + 2) {
            a[i] += dif;
        }
        for (i, xs) in a.windows(3).enumerate() {
            assert_eq!(xs.iter().sum::<i64>(), s[i]);
        }

        Some(a)
    }
}

fn main() {
    setup_out!(put, puts);

    if let Some(res) = solve() {
        puts!("Yes");
        for r in res {
            put!("{} ", r);
        }
        puts!("");
    } else {
        puts!("No");
    }
}


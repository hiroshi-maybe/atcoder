fn solve() -> bool {
    let _n = readln!(usize);
    let a = readln!([usize]);

    let mut flipped = false;
    let mut q = a.iter().copied().collect::<VecDeque<_>>();
    while let Some(&tail) = q.back() {
        let val = |v: usize| v ^ (flipped as usize);
        if val(tail) == 0 {
            q.pop_back();
        } else {
            let fr = val(q.pop_front().unwrap());
            if fr != 0 {
                return false;
            } else {
                flipped = !flipped;
            }
        }
    }

    true
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", if res { "Yes" } else { "No" });
}


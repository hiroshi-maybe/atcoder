fn solve() -> bool {
    let (n, x, y) = readln!(usize, isize, isize);
    let a = readln!([usize]);

    let offset = 10_010;
    let (x, y) = ((x + offset) as usize, (y + offset) as usize);
    let offset = offset as usize;
    let maxx = 2 * offset;
    let mut dp = vvec!(false; 2; maxx);
    dp[0][offset + a[0]] = true;
    dp[1][offset] = true;
    for i in 1..n {
        let mut dp_new = vec![false; maxx];
        for xy in 0..maxx {
            if dp[i % 2][xy] {
                dp_new[xy + a[i]] = true;
                dp_new[xy - a[i]] = true;
            }
        }
        dp[i % 2] = dp_new;
    }

    dp[0][x] && dp[1][y]
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", if res { "Yes" } else { "No" });
}


fn solve() -> String {
    let n = readln!(usize);
    let s = readln!(String);

    s.replacen("A", "BB", n).replacen("BB", "A", n)
}

#[allow(dead_code)]
fn solve_v0() -> String {
    let n = readln!(usize);
    let s = readln!([char]);

    let mut res = vec![];
    let mut l = 0;
    while l < n {
        let r = s[l..].iter().position(|&c| c == 'C').map_or(n, |i| l + i);
        // dbgln!(l, r);
        let cnt_b = s[l..r].iter().filter(|&&c| c == 'B').count();
        let cnt_a = r - l - cnt_b;
        res.extend(vec!['A'; cnt_a + cnt_b / 2]);
        res.extend(vec!['B'; cnt_b % 2]);
        //dbgln!(l, r, &s[r..]);
        if r < n {
            let ll = s[r..].iter().position(|&c| c != 'C').map_or(n, |i| i + r);
            res.extend_from_slice(&s[r..ll]);
            l = ll;
        } else {
            break;
        }
    }

    res.iter().collect::<String>()
}

fn main() {
    setup_out!(put, puts);

    let res = solve();
    puts!("{}", res);
}

